#!/usr/bin/env node
/**
 * sync-cms-config — build-time read of pandotic-site's enabled modules
 * from the Pandotic Hub, written to `src/cms.config.generated.ts` for
 * consumption by `cms.config.ts`.
 *
 * Runs as part of `pnpm build` (see package.json). When the Hub is
 * unreachable or env vars are missing, writes a sentinel file so
 * cms.config.ts falls back to its hardcoded defaults — never blocks
 * the build.
 *
 * Required env vars (set in Netlify build env):
 *   HUB_SUPABASE_URL        — e.g. https://rimbgolutrxpmwsoswhq.supabase.co
 *   HUB_SUPABASE_ANON_KEY   — anon key with RLS-permitted read on
 *                             hub_properties.enabled_modules for slug
 *                             'pandotic-site'
 */

import { writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const OUTPUT_PATH = resolve(
  __dirname,
  "..",
  "src",
  "cms.config.generated.ts",
);
const PROPERTY_SLUG = "pandotic-site";

const url = process.env.HUB_SUPABASE_URL;
const key = process.env.HUB_SUPABASE_ANON_KEY;
const generatedAt = new Date().toISOString();

async function fetchHubModules() {
  if (!url || !key) {
    console.warn(
      "[sync-cms-config] HUB_SUPABASE_URL or HUB_SUPABASE_ANON_KEY not set; using local fallback.",
    );
    return null;
  }
  try {
    const endpoint = `${url.replace(/\/$/, "")}/rest/v1/hub_properties?slug=eq.${encodeURIComponent(
      PROPERTY_SLUG,
    )}&select=enabled_modules,last_module_sync_at`;
    const res = await fetch(endpoint, {
      headers: {
        apikey: key,
        Authorization: `Bearer ${key}`,
        Accept: "application/json",
      },
    });
    if (!res.ok) {
      console.warn(
        `[sync-cms-config] Hub returned HTTP ${res.status}; using local fallback.`,
      );
      return null;
    }
    const rows = await res.json();
    if (!Array.isArray(rows) || rows.length === 0) {
      console.warn(
        `[sync-cms-config] No hub_properties row for slug '${PROPERTY_SLUG}'; using local fallback.`,
      );
      return null;
    }
    const enabled = rows[0].enabled_modules;
    if (!Array.isArray(enabled)) {
      console.warn(
        "[sync-cms-config] enabled_modules is not an array; using local fallback.",
      );
      return null;
    }
    return enabled.filter((v) => typeof v === "string");
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.warn(`[sync-cms-config] Hub fetch failed: ${msg}; using local fallback.`);
    return null;
  }
}

const enabled = await fetchHubModules();

const banner = `// AUTO-GENERATED by scripts/sync-cms-config.mjs — do not edit by hand.
// Source: Pandotic Hub property '${PROPERTY_SLUG}' enabled_modules.
// To refresh: \`pnpm sync-cms-config\` (pulls latest from Hub).
`;

const body =
  enabled === null
    ? `${banner}
export const HUB_ENABLED_MODULES: readonly string[] | null = null;
export const HUB_GENERATED_AT: string | null = null;
`
    : `${banner}
export const HUB_ENABLED_MODULES: readonly string[] | null = ${JSON.stringify(
        enabled,
        null,
        2,
      )} as const;
export const HUB_GENERATED_AT: string | null = ${JSON.stringify(generatedAt)};
`;

await mkdir(dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, body);

console.log(
  enabled === null
    ? "[sync-cms-config] Wrote sentinel (no Hub data) — cms.config will use local fallback."
    : `[sync-cms-config] Wrote ${enabled.length} enabled module${
        enabled.length === 1 ? "" : "s"
      } from Hub: ${enabled.join(", ") || "(none)"}.`,
);
